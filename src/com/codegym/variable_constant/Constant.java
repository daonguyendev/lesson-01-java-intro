package com.codegym.variable_constant;

public class Constant {

    public static void main(String[] args) {
        // Khai báo hằng trong Java yêu cầu:
        // 1. Cần phải có từ khóa final khi khai báo
        // 2. Tên phải có ý nghĩa cho việc nó làm (meaningful name)
        // 3. Phải khởi tạo gi trị mặc định cho hằng khi khai báo
        // 4. Tên phải được viết hoa toàn bộ và sử dụng quy tắc SNAKE_CASE để đặt tên
        final int MAX_SPEED_IN_CITY = 40;
        System.out.println(MAX_SPEED_IN_CITY);
    }
}
